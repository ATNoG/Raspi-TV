// Helper methods
////////////////////////////////////////////////////////

// Updates user's info
//
function getUser() {
    $.get('/admin/user', function (data) {
        data = JSON.parse(data);
        for (var key in data)
            $('.' + key).html(data[key]);
    });
}

////////////////////////////////////////////////////////
// Main 'when ready' method
////////////////////////////////////////////////////////

$(document).ready(function () {
    getUser();
});
